from CheckersTournament.game_elements.board import Square, Vector, Move, Board

def test_square():
    s1 = Square(1, 1)
    s2 = Square(1, 1)
    assert s1 == s2
    v1 = Vector(1, 1)
    s3 = Square(2, 2)
    assert s1 + v1 == s3


def test_move():
    s1 = Square(1, 1)
    s2 = Square(2, 2)
    m1 = Move(s1, s2)
    s1 = Square(1, 1)
    s2 = Square(2, 2)
    m2 = Move(s1, s2)
    assert m1 == m2


